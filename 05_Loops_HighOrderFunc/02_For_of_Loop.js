// ---------------- For of Loop

// Loop over arrays

const items = ["book", "Table", "chair", "kite"];

for (const item of items) {
    // console.log(item);
}

// Loop over Objects

const users = [{ name: "brad" }, { name: "kate" }, { name: "Steve" }];

for (const user of users) {
    console.log(user.name);
}

// Loop over strings

const str = "Hello world";

for (const letter of str) {
    console.log(letter);
}
